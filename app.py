def get_latest_pricing():
    """–ü–æ–ª—É—á–∞–µ—Ç –∞–∫—Ç—É–∞–ª—å–Ω—ã–µ —Ü–µ–Ω—ã –∏ –¥–∞—Ç—ã –ø—Ä–æ–±–Ω—ã—Ö –∑–∞–Ω—è—Ç–∏–π –∏–∑ –ë–î"""
    with engine.connect() as conn:
        result = conn.execute(text("SELECT price, trial_days, trial_time, subscription_details FROM pricing LIMIT 1"))
        data = result.fetchone()

        if data:
            price, trial_days, trial_time, subscription_details = data
            print(f"–¶–µ–Ω–∞: {price}, –î–Ω–∏ –ø—Ä–æ–±–Ω—ã—Ö –∑–∞–Ω—è—Ç–∏–π: {trial_days}, –í—Ä–µ–º—è: {trial_time}, –î–µ—Ç–∞–ª–∏: {subscription_details}")
            return {
                "price": price,
                "trial_days": trial_days,
                "trial_time": trial_time,
                "subscription_details": subscription_details
            }
        else:
            print("–ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–∞–±–ª–∏—Ü–µ pricing")
            return None

